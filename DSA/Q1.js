
//     The Senior Citizen
//      Description
//      You are given a number stored in a variable, with the name age
//      Check whether the age is greater than and equal to 60 or not
//      If true, Then print "Senior Citizen"
//      otherwise "Not Senior Citizen"
//     Output
//     If the value stored in age , is greater than and equal to 60 Then print Senior Citizen, else print not Senior Citizen
//     Hint
//     the sample input, age = 50
//     Since 50 is less than 60.

let age = 50 ;
if(age>=60){
    console.log("Senior Citizen");
}
else{
    console.log("Not Senior Citizen");
}

